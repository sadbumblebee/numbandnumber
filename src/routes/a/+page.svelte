<h1>So what exactly is this?</h1>
<p>Honestly, beyond the title, I do not know. This idea started as a list. A list of stories that I wanted to publish, but couldn't find the right platform for them in my current work. As ideas stacked up, I felt that perhaps I could challenge myself by creating a tidy environment to publish them. I thought: What a nice way to keep my talents sharp in Svelte, while also experimenting in a low-risk environment.</p>
<p>And now here we are.</p>
<p>I am sure that in the future what <em>works</em> on this site and what "doesn't" will become apparent. Maybe I'll shift and do more of that work. But for now, the work — as is this about page — is simply presented as is.</p>
<p>If you would like to see more of a particular type of post, feel free to reach out to me and let me know.</p>
<p>— Daniel</p>

